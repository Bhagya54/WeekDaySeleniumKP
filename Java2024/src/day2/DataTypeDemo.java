package day2;

public class DataTypeDemo {

	
	/*
	 * Primitive DataType
	 * Integer: byte,short,int,long
	 * Decimal : float,double
	 * character: char
	 * boolean: true/false
	 * 
	 * Non-Primitive DataType -- Class based
	 * String
	 * Array
	 * ArrayList
	 * 
	 */

	
	public static void main(String[] args) {
		int var1 = 23;
		byte b1 = 127;
		short s1 = 32767;
		
		float f1 = 34.53423f;
		double d1 = 576876.4546;
		
		char c1 = 'k';
		boolean b2 = false;
		
		String s2 = "testing";
		System.out.println(s2);
		s2 = "bhagya";
		System.out.println(s2);
		
		System.out.println();

	}

}
